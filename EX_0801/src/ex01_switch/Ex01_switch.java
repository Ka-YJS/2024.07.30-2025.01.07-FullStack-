package ex01_switch;

public class Ex01_switch {

	public static void main(String[] args) {
		
		int num = 7;
		
		switch(num) {
		case 1 : 
			System.out.println("num은 1입니다.");
			break;
		case 7 : 
			System.out.println("num은 7입니다.");
			break;
		default :
			System.out.println("num은 7도 1도 아닙니다.");
			break;
		}
		
		
		System.out.println("----------------------");
		
	
		

	}

}
