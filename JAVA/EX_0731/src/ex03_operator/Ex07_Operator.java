package ex03_operator;

public class Ex07_Operator {

	public static void main(String[] args) {

		
		//삼항연산자
		
		
		int a = 10;
		int b = 15;	
		
		boolean result = ++a >= b ? true : false; // 11 >= 15
		System.out.println(result);
		
		char result2 = (a+=b) == b? 'o' : 'x'; // 11+15 = 15
		System.out.println(result2);
		
		
		
		
		
		
		
		
		
	}

}
