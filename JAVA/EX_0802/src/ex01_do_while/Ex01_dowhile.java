package ex01_do_while;

public class Ex01_dowhile {

	public static void main(String[] args) {
		
		//do-while문
		
		int i = 11;
		do{
		    System.out.println(i);

		}while(i <= 10);
		
		
		System.out.println("--------------");
		
		
		int sum = 0;
		int n = 1;
		
		do {
			sum+=n;
			n++;
		}while(n<=10);
		
		System.out.println("합 : "+sum);
		

		System.out.println("--------------");
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
