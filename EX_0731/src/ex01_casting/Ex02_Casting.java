package ex01_casting;

public class Ex02_Casting {

	public static void main(String[] args) {
		
		int i = 10;
		byte b = (byte)i;
		
		System.out.printf("[int->byte] i의 값 : %d , b의 값 : %d \n",i,b);
		
		
		int j = 1000;
		byte c = (byte)j;
		
		System.out.printf("[int->byte] j의 값 : %d , c의 값 : %d ",j,c);
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
