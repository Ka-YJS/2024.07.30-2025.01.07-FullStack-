package ex01_casting;

public class Ex04_Casting {

	public static void main(String[] args) {
		
		
		float f1 = 123645.67f;
		int i1 = (int)f1;
		
		System.out.println("[float->int] f1의 값 : " +f1+", i1의 값 : "+i1);
		
		
		System.out.println("=========================");

		double d1 = 12345.67;
		int i2 = (int)d1;
		
		System.out.println("[double->int] d1의 값 : " +d1+", i2의 값 : "+i2);
		
		
		System.out.println("=========================");
		
		byte b1 = 100;
		byte b2 = 20;
		
		int i4 = b1+b2;
		
		System.out.println(i4);
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
